package ex1;

public class Calcul {

    /**
     * @param a integer to add
     * @param b integer to add
     * @return the sum of the two parameters a and b
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * @param a integer to substract
     * @param b integer to substract
     * @return the substraction of the two parameters a and b
     */
    public int substract(int a, int b) {
        return a - b;
    }
}
